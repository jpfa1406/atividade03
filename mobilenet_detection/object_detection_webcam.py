import cv2

#cap = cv2.VideoCapture('hall_box_battery_1024.mp4')
cap = cv2.VideoCapture(0)
#make_1080p()
#make_720p()
make_480p()

print("Known classes")
print(CLASSES)

while(True):
    # Capture frame-by-frame
    ret, frame = cap.read()
    
    result_frame, result_tuples = detect(frame)

    
    #cv2.putText(result_frame,fps,(10,500), font, 4, (255, 255, 255), 2, cv2.LINE_AA)
    # Display the resulting frame
    

    # Prints the structures results:
    # Format:
    # ("CLASS", confidence, (x1, y1, x2, y3))
    classes = []
    for t in result_tuples:
        classes.append(t[0])

    for t in result_tuples:
        if str(t[0]) == 'person':
            tempo += 1
            if tempo >= 20:
                cv2.rectangle(result_frame, (t[2][0],t[2][1]),(t[3][0],t[3][1]), (0,0,255),5)
    
    if 'person' not in classes:
        tempo = 0
    
    
    cv2.imshow('frame',result_frame)

    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

# When everything done, release the capture
cap.release()
cv2.destroyAllWindows()
